package com.August19;

public class SwitchCasePgm {

	public static void main (String [] args ){
			int Holliday = 819 ;
			
		switch (Holliday) {
		
		case 815 : 	System.out.println("Independence Day"); 	break;
		case 820 :	System.out.println("Sunday");				break;
		default: 	System.out.println("Working day");			break;
				
		}
	}
}
